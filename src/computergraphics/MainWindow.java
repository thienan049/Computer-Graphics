    public void greeting()
    {
        if (grtRunBack == true) {
            this.SttBar.repaint();
            grtLocation -= step;
            if (grtLocation == 10) {
                grtRunBack = false;
            }
        } else {
            this.SttBar.repaint();
            grtLocation += step;
            if (grtLocation == 603) {
                grtRunBack = true;
            }
        }  
    }
    
