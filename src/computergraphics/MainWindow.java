    public int scaledX(double originalValue) // tr v ta  X (lm trn) theo t l 5:1 vi gc TD 400;250
    {
        if (originalValue < originX) {
            return (int) ((originalValue - originX) / 5 - 0.5);
        } else {
            return (int) ((originalValue - originX) / 5 + 0.5);
        }
    }

    public int scaledY(double originalValue) // tr v ta  Y (lm trn) theo t l 5:1 vi gc TD 400;250
    {
        if (originalValue < originY) {
            return (-1) * (int) ((originalValue - originY) / 5 - 0.5);
        } else {
            return (-1) * (int) ((originalValue - originY) / 5 + 0.5);
        }
    }

